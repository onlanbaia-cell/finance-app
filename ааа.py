# -------------------------------
# 1.1. Айнымалылар мен типтерді қолдану
# -------------------------------

print("=== ҚАРЖЫ ШЫҒЫНДАРЫН ЕСЕПТЕУ ҚОСЫМШАСЫ ===")

айлық_табысы = float(input("Айлық табысыңызды енгізіңіз (тг): "))
тамақ = float(input("Тамаққа кеткен шығын (тг): "))
көлік = float(input("Көлікке кеткен шығын (тг): "))
басқа = float(input("Басқа шығындар (тг): "))

барлығы = тамақ + көлік + басқа
қалдық = айлық_табысы - барлығы

print(f"\nЖалпы шығын: {барлығы} тг")
print(f"Қалған ақша: {қалдық} тг")

if қалдық < 0:
    print("⚠️ Сіз минусқа кеттіңіз! Шығындарды азайтыңыз.")
else:
    print("✅ Қаржылық жағдай тұрақты.")

# -------------------------------
# 1.2. Күрделі деректер типтері
# -------------------------------

шығын_санаттары = ["тамақ", "көлік", "коммуналдық", "көлік", "киім", "киім"]
print("\nБастапқы тізім:", шығын_санаттары)

бірегей_шығындар = set(шығын_санаттары)
print("Бірегей шығын түрлері:", бірегей_шығындар)

валюталар = ("теңге", "доллар", "евро")
print("Қолданылатын валюталар:", валюталар)

# -------------------------------
# 1.3. Мәтіндік деректер мен логикалық шешім
# -------------------------------

санат = input("\nҚай санаттағы шығынды іздейсіз? ").lower()
if санат in бірегей_шығындар:
    print(f"{санат} санаты бар ✅")
else:
    print(f"{санат} санаты табылмады ❌")

мәтін = "Ай сайынғы шығындар: тамақ, көлік, коммуналдық, киім"
жаңартылған = мәтін.replace("киім", "демалыс")
print("Жаңартылған мәтін:", жаңартылған)
print("Мәтінді бөлу:", жаңартылған.split(","))

# -------------------------------
# 1.4. Dict және циклдер
# -------------------------------

шығындар = {
    "тамақ": 30000,
    "көлік": 15000,
    "коммуналдық": 10000
}

print("\n=== Шығындар тізімі ===")
for атау, сома in шығындар.items():
    print(f"{атау}: {сома} тг")

# Пайдаланушы мәзірі
while True:
    print("\n--- Мәзір ---")
    print("1 - Шығын қосу")
    print("2 - Барлық шығындарды көру")
    print("3 - Жалпы есеп шығару")
    print("4 - Шығу")

    таңдау = input("Таңдаңыз (1-4): ")

    if таңдау == "1":
        атау = input("Шығын атауы: ").lower()
        сома = float(input("Сомасы (тг): "))
        шығындар[атау] = сома
        print(f"{атау} шығыны қосылды ✅")

    elif таңдау == "2":
        print("\nБарлық шығындар:")
        for атау, сома in шығындар.items():
            print(f"- {атау}: {сома} тг")

    elif таңдау == "3":
        жалпы_шығын = sum(шығындар.values())
        қалдық = айлық_табысы - жалпы_шығын
        print(f"\nЖалпы шығын: {жалпы_шығын} тг")
        print(f"Қалған ақша: {қалдық} тг")
        if қалдық < 0:
            print("⚠️ Минусқа кеттіңіз!")
        else:
            print("✅ Тепе-теңдік сақталуда.")

    elif таңдау == "4":
        print("Бағдарлама аяқталды. Қайта келіңіз!")
        break

    else:
        print("Қате таңдау! Қайта көріңіз.")
